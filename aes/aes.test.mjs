import test from 'ava';

import {subBytes} from "./aes.mjs";

test('subBytes', t => {
    let state = [
        [0x00, 0x01, 0x02, 0x03],
        [0x04, 0x05, 0x06, 0x07],
        [0x08, 0x09, 0x0a, 0x0b],
        [0xfc, 0xfd, 0xfe, 0xff],
    ];

    subBytes(state);

    t.like(state, [
        [0x63, 0x7c, 0x77, 0x7b],
        [0xf2, 0x6b, 0x6f, 0xc5],
        [0x30, 0x01, 0x67, 0x2b],
        [0xb0, 0x54, 0xbb, 0x16],
    ])
})

